package com.assignment.demo;

public class AssignmentOperators {
	public static void main (String[] args) {
		int var1 = 25;
		int var2 = 20;
		System.out.println("var1 = " + var1 + "\n" + "var2 = " + var2);
		var1 += var2;
		System.out.println("var1 = " + var1);
		var1 -= var2;
		System.out.println("var1 = " + var1);
		var1 *= var2;
		System.out.println("var1 = " + var1);
		var1 /= var2;
		System.out.println("var1 = " + var1);
		
	}
}
